class Flows {
    public static void main(String[] args) {
        // We define a variable 'number' and set it to 4.
        int number = 4;

        // We check if 'number' is greater than or equal to 4.
        if (number >= 4) {
            // If the condition is true, we print this message.
            System.out.println("number is greater than or equal to 4");
        } else {
            // If the condition is false, we print this message.
            System.out.println("number is less than 4");
        }
    }
}

// The output of this program will be:
// number is greater than or equal to 4
// This is because the value of 'number' is 4, which meets the condition.